public enum CategoryLevel {

    /** Level for the player begins at. Any amount sets the level to novice */
    NOVICE,

    /** Level for a combined total of less than 50 years */
    INTERMEDIATE,

    /** Level for a combined total of less than 25 years */
    EXPERT,

    /** Level for a combined total of less than 10 years */
    HISTORY_MASTER,

    /** Level for a combined total of 0 years off. A true master. */
    HISTORIAN_GOD;
}
